/// Passes the flag `-explain-module-dependency-detailed`
///
public struct ExplainModuleDependencyDetailed: UnsafeFlag {
  public let value: String

  public var unsafeFlagArguments: [String] {
    ["\(name.camelToSnakeCaseFlag())", "\(value)"]
  }

  public init(_ value: String) {
    self.value = value
  }
}
