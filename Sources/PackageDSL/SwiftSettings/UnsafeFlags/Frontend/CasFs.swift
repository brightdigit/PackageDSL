/// Passes the flag `-cas-fs`
///  Root CASID for CAS FileSystem
public struct CasFs: FrontendFlag {
    public let value: String

    public init(_ value: String) {
        self.value = value
    }

    public var flagArguments: [String] {
        ["\(name.camelToSnakeCaseFlag())", "\(value)"]
    }
}
